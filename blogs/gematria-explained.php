          <h3>Common English Ciphers</h3>
          <p>When the practice was adapted for English, several ciphers were created. The two most common are:</p>
          <ul>
            <li><strong>English Ordinal:</strong> The simplest and most intuitive system. The letters are assigned values based on their alphabetical position: A=1, B=2, C=3, ... Z=26.</li>
            <li><strong>English Reduction (or Pythagorean):</strong> This system reduces every letter to a single digit from 1-9. The cycle repeats after the letter I. So, A=1, I=9, and then J=1, K=2, and so on. This method focuses on the foundational "digital root" of each letter.</li>
          </ul>

