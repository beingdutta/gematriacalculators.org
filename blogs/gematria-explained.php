          <p>The "engine" of Gematria is its cipher the specific map of letter-to-number assignments. While dozens of variations exist, a few key systems form the foundation of most Gematria work. Knowing them is essential for any exploration.</p>

          <h3>The Standard Hebrew System (Mispar Hechrechi)</h3>
          <p>This is the classic and most common system used in rabbinic and Kabbalistic texts. It follows a clear, logical pattern based on the order of the 22 Hebrew letters:</p>
          <ul>
            <li>The first 9 letters are assigned the units 1-9.</li>
            <li>The next 9 letters are assigned the tens 10-90.</li>
            <li>The final 4 letters are assigned the hundreds 100-400.</li>
          </ul>

